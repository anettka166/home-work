package assignment8_loops;

public class One2 {

    public static void main(String[] args) {
        // 1. Using the FOR loop print the even numbers from 500 to 1000 according to the below example
        //    e.g
        //    0 -> 2 -> 4 -> 6 .... -> 1000

        for(int i = 500; i <= 1000; i++){
            if(i % 2 ==0){
                System.out.print(i + "-->");
            }
        }








    }
}
